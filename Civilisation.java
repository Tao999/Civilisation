
public class Civilisation {
	private int m_id;
	private int m_nbWood;
	private int m_nbMinerals;
	
	Civilisation(){
		
	}

	public int getId() {
		return m_id;
	}

	public void setId(int m_id) {
		this.m_id = m_id;
	}

	public int getNbWood() {
		return m_nbWood;
	}

	public void setNbWood(int m_nbWood) {
		this.m_nbWood = m_nbWood;
	}

	public int getNbMinerals() {
		return m_nbMinerals;
	}

	public void setNbMinerals(int m_nbMinerals) {
		this.m_nbMinerals = m_nbMinerals;
	}
	
	
}
